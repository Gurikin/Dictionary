    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        if (evt.getKeyChar() == ' ') {
            listWordPair('+');
        }
        if (evt.getExtendedKeyCode() == 8) {
            listWordPair('-');
        }
    }//GEN-LAST:event_formKeyPressed
                            
    private void listWordPair(char backForward) {
        String[] currentWords;
        if (backForward == '+') {
            currentWords = Dictionary.learnWords(i++);
        } else {
            currentWords = Dictionary.learnWords(i--);
        }
        if (i < 0) {
            i = 0;
        }        
        String currentEnglishWord = currentWords[0];
        String currentRussianWord = currentWords[1];
        EngLearnLabel.setText(currentEnglishWord);
        RusLearnLabel.setText(currentRussianWord);
    }
    
